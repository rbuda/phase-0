/* Once the votes have been tallied, assign each officer position the name of the
student who received the most votes. */
var officers = {
  president: undefined,
  vicePresident: undefined,
  secretary: undefined,
  treasurer: undefined
}

// Pseudocode
// FOR each name in votes
//   FOR each position in voter
//     IF canditate exists
//       candidate += 1
//     ELSE new property candidate with value 1
//     ENDIF
//   ENDFOR
// Pt 2
// iterate over properties within voteCount object to find max value
//   start with the voteCount object
//    for every property within the object,
//      iterate over every property-value pair within that property
//        save the first value as the highest
//        if a higher value is found, replace the saved value with new highest
//    assign the property matching the high value to the matching property within the officers object
// _______________________________________
// Initial Solution

var tally = function(object) {
  for (var voter in object) {
    var ballot = object[voter]; //ballot is the object consisting of each voter's votes - who for what
    for (var position in ballot) {
      votes = ballot[position] // who specifically was voted into each position
      if (voteCount[position].hasOwnProperty(ballot[position])) {
        voteCount[position][votes] += 1;
      } else {
        voteCount[position][votes] = 1;
      }
    }
  }
  for (position in voteCount) {
    var highest = 0;
    var winner = "";
    for (var candidate in voteCount[position]) {
      if (voteCount[position][candidate] > highest) {
        highest = voteCount[position][candidate];
        winner = candidate;
      }
    }
    officers[position] = winner;
  }
}
tally(votes);

